package com.trimble.stripewrapper.dtos;

public class PaymentIntentInputDTO {

    private String secretKey;
    private String publicKey;
    private String accountId;
    private Long amount;
    private String currency;
    private String customerName;
    private String customerEmail;
    private CardTokenDTO cardTokenDTO;
    private String returnUrl;

    public PaymentIntentInputDTO() {
    }

    public String getSecretKey() {
        return secretKey;
    }

    public void setSecretKey(String secretKey) {
        this.secretKey = secretKey;
    }

    public String getPublicKey() {
        return publicKey;
    }

    public void setPublicKey(String publicKey) {
        this.publicKey = publicKey;
    }

    public String getAccountId() {
        return accountId;
    }

    public void setAccountId(String accountId) {
        this.accountId = accountId;
    }

    public String getCustomerName() {
        return customerName;
    }

    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    public String getCustomerEmail() {
        return customerEmail;
    }

    public void setCustomerEmail(String customerEmail) {
        this.customerEmail = customerEmail;
    }

    public CardTokenDTO getCardTokenDTO() {
        return cardTokenDTO;
    }

    public void setCardTokenDTO(CardTokenDTO cardTokenDTO) {
        this.cardTokenDTO = cardTokenDTO;
    }

    public Long getAmount() {
        return amount;
    }

    public void setAmount(Long amount) {
        this.amount = amount;
    }

    public String getCurrency() {
        return currency;
    }

    public void setCurrency(String currency) {
        this.currency = currency;
    }

    public String getReturnUrl() {
        return returnUrl;
    }

    public void setReturnUrl(String returnUrl) {
        this.returnUrl = returnUrl;
    }
}
